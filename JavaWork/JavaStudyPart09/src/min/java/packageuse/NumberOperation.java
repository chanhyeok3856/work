package min.java.packageuse;

public class NumberOperation {
	public static void main(String[] args) {
		// 4의 자연 로그 값은 연산하고 출력한다.
		System.out.println("4의 자연로그값 : " + Math.log(4));
		// 9의 제곱근을 연산하고 출력한다.
		System.out.println("9의 제곱근 값 : " + Math.sqrt(9));
		// 3의 4승 값을 연산하고 출력한다.
		System.out.println("3의 4승 값: " + Math.pow(3, 4));
		// 3.14579을 반올림한 정수값을 출력한다.
		System.out.println("3.14579의 정숫값 : " + Math.round(3.14579));
	}
}